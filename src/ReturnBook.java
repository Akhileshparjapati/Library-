        // TODO add your handling code here:
        if(txtstudentid.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Enter book id for searching");
            txtstudentid.requestFocus();
        }
        else{
            try {
                pst = con.prepareStatement("UPDATE BOOKS SET STATUS=?,ISSUEDATE=?,DUEDATE=?,studentid=? WHERE BOOKS.ID=?");
                pst.setString(1, "ISSUED");
                pst.setString(2,"");
                pst.setString(3,"");
                pst.setString(4,txtstudentid.getText());
                pst.setString(5,txtbookid.getText());
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(this,"Book Returned");  
                txtstudentname.setText("");
                txtstudentid.setText("");
                txtbookid.setText("");
                txtbookname.setText("");
                txtissuedate.setText("");
                txtduedate.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
