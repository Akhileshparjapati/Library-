    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(txtstudentid.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Enter book id for search it");
            txtstudentid.requestFocus();
        }
        else{
            try {
                pst = con.prepareStatement("select student.name,books.id,books.name,books.issuedate,books.duedate from books inner join student using(id) where books.id = ?;"); 
                pst.setString(1, txtstudentid.getText());
                rsp=pst.executeQuery();
                if(rsp.next()){
                    txtbookid.setText(rsp.getString(2));
                    txtstudentname.setText(rsp.getString(1));
                    txtissuedate.setText(rsp.getString(4));
                    txtduedate.setText(rsp.getString(5));
                    txtbookname.setText(rsp.getString(3));
                    
                    
                    
                }
                else{
                    JOptionPane.showMessageDialog(this, "Please enter valid dtudent id");
                }
                
            
            }
            catch (SQLException ex) {
                Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

